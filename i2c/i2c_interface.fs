20 CONSTANT I2C_P

20 CONSTANT I2C_PORT
81 CONSTANT I2C_QUIES
0 VARIABLE I2C_BANK

01 CONSTANT SCLBIT
80 CONSTANT SDABIT

: RL
    2 * 100 /MOD 
;

: I2C_RDPORT
    I2C_PORT IN
;

: I2C_WRPORT
    DUP
    I2C_PORT OUT
    I2C_BANK !
;

: I2C_SCL_LO
    I2C_BANK @
    FE AND
    I2C_WRPORT
;

: I2C_SCL_HI
    I2C_BANK @
    01 OR
    I2C_WRPORT
;

: I2C_SDA_LO
    I2C_BANK @
    7F AND
    I2C_WRPORT
;

: I2C_SDA_HI
    I2C_BANK @
    80 OR
    I2C_WRPORT
;

: I2C_INIT
    I2C_QUIES I2C_WRPORT
;

: I2C_START
    I2C_SDA_HI
    I2C_SCL_HI
    I2C_SDA_LO
    I2C_SCL_LO
;

: I2C_STOP
    I2C_SDA_LO
    I2C_SCL_HI
    I2C_SDA_HI
;

: I2C_WAIT
    BEGIN
	I2C_RDPORT
	SCLBIT AND
    UNTIL
;

: I2C_READ
    0 
    8 0 DO
	2 *
	I2C_SDA_HI
	I2C_SCL_HI
	I2C_WAIT
	I2C_RDPORT
	SDABIT AND IF
	    1+
	THEN
	I2C_SCL_LO
    LOOP

    SWAP IF I2C_SDA_LO ELSE I2C_SDA_HI THEN

    I2C_SCL_HI
    I2C_SCL_HI
    I2C_SCL_LO	
;

: I2C_WRITE
    8 0 DO 
	RL 
	IF I2C_SDA_HI ELSE I2C_SDA_LO THEN
	I2C_SCL_HI
	I2C_SCL_LO
    LOOP

    DROP

    I2C_SDA_HI
    I2C_SCL_HI
    I2C_RDPORT
    I2C_SCL_LO
    SDABIT AND
    0= 1-
;

: I2C_OPEN
    I2C_START
    I2C_WRITE
;

: I2C_CLOSE
    I2C_STOP
;

48 CONSTANT LM75A_7BIT
LM75A_7BIT 2 * CONSTANT LM75A_ADDR

: LM_READ
    LM75A_ADDR I2C_OPEN

    DUP 0= IF
	1 I2C_WRITE DROP
	0 I2C_WRITE DROP
	I2C_STOP

	LM75A_ADDR I2C_OPEN

	DUP 0= IF
	    0 I2C_WRITE DROP
	    LM75A_ADDR 1+ I2C_OPEN DROP
	    0 I2C_READ
	    0 I2C_READ
	    I2C_STOP
	ELSE	
	    FF	
	THEN
    ELSE
	-1
    THEN
;   
