DECIMAL 16 BASE C! ( SWITCH TO HEX )

20 CONSTANT I2C_PORT ( CONTROL PORT FOR SC137 INTERFACE )
81 CONSTANT I2C_QUIES ( FLOATING VALUE FOR SC137 INTERFACE )
0 VARIABLE I2C_BANK ( STORE FOR COPY OF DATA WRITTEN TO I2C )

: I2C_RDPORT ( -- N )
    I2C_PORT IN
;

: I2C_WRPORT ( N -- )
    DUP I2C_PORT OUT
    I2C_BANK !
;

: I2C_SDA_LO
    I2C_BANK @
    7F AND ( RESET BIT 7 )
    I2C_WRPORT
;

: I2C_SDA_HI
    I2C_BANK @
    80 OR ( SET BIT 7 )
    I2C_WRPORT
;

: I2C_SCL_LO
    I2C_BANK @
    FE AND ( RESET BIT 0 )
    I2C_WRPORT
;

: I2C_SCL_HI
    I2C_BANK @
    01 OR ( SET BIT 0 )
    I2C_WRPORT
;
        

( TEST IF I2C MASTER IS PRESENT )
: I2C_MTEST ( -- FLAG )
    I2C_SDA_LO
    I2C_RDPORT
    80 XOR ( SHOULD BE %1XXXXXXX, IF INTERFACE )

    I2C_SDA_HI
    I2C_RDPORT
    80 AND ( SHOULD BE 80, IF INTERFACE )

    AND 80 =
;

( INITIALISE DEVICE )
: I2C_INIT ( -- )
    I2C_QUIES
    I2C_WRPORT
;

( 8-BIT ROTATE LEFT )
: RL ( N -- CF N*2 )
    2 * 100 /MOD 
;


: I2C_START ( -- )
    I2C_SDA_LO
    I2C_SCL_LO
;

: I2C_STOP ( -- )
    I2C_SDA_LO I2C_SCL_LO I2C_SCL_HI I2C_SDA_HI
;


: I2C_READ ( FL -- N )
    0
    8 0 DO
	I2C_SDA_HI
	I2C_SCL_HI
	2 *
	I2C_RDPORT 80 AND IF 1+ THEN
	I2C_SCL_LO
    LOOP

    SWAP IF I2C_SDA_LO THEN
    I2C_SCL_HI
    I2C_SCL_LO	
;

	
: I2C_WRITE ( N -- FLAG )
    8 0 DO
	RL IF I2C_SDA_HI ELSE I2C_SDA_LO THEN
	I2C_SCL_HI
	I2C_SCL_LO
    LOOP

    DROP
    
    I2C_SDA_HI
    I2C_SCL_HL
    I2C_RDPORT
    I2C_SCL_LO
    80 AND

    IF
	I2C_SDA_LO
	I2C_SCL_HI
	I2C_SDA_HI
	-1
    ELSE
	0
    THEN
;    
    
: I2C_OPEN ( addr -- )
    I2C_START I2C_WRITE
;

: I2C_CLOSE ( -- )
    I2C_STOP
;

48 CONSTANT LM75A_7BIT
LM75A_7BIT 2 * CONSTANT LM75A_ADDR

: LM_SETUP ( -- )
    LM75A_ADDR
    I2C_OPEN

    DUP 0= IF 
	1 I2C_WRITE
	0 I2C_WRITE
	I2C_STOP
    THEN
;

: LM_READ ( -- )
    LM75A_ADDR
    I2C_OPEN

    DUP 0= IF 
	0 I2C_WRITE DROP
	LM75A_ADDR 1+
	I2C_OPEN DROP
	0 I2C_READ
	0 I2C_READ
	I2C_STOP
    THEN
;
